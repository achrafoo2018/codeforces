void solve(){
	int t;
	cin >> t;
	while(t--){
		ll x1, x2, x3, y1, y2, y3;
		cin >> x1 >> y1 >> x2 >> y2 >> x3 >> y3;
		x2 -= x1;
		x3 -= x1;
		y2 -= y1;
		y3 -= y1;
		ll ans = x2*y3 - x3*y2;
		if(ans == 0)
			cout << "TOUCH" << endl;
		else if(ans > 0)
			cout << "LEFT" << endl;
		else
			cout << "RIGHT" << endl;	
	}
}

