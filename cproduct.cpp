#define For(i, n) for(int i=0; i < n; ++i)

struct Point{
	ll x, y;
	void read(){
		cin >> x >> y;
	}
	Point operator -(const Point& b) const{
		return Point{x- b.x, y-b.y};
	}
	void operator -= (const Point& b){
		x -= b.x;
		y -= b.y;
	}
	ll operator *(const Point& b) const{
		return x * b.y - y *b.x;
	}
};

void solve(){
	Point p1, p2, p3;
	p1.read();
	p2.read();
	p3.read();
	p2 -= p1;
	p3 -= p1;
	ll ans = p2*p3;
	if(ans == 0)
		cout << "TOUCH\n";
	else if(ans > 0)
		cout << "LEFT\n";
	else
		cout << "RIGHT\n";
}

int main(){
	fastio;
	// freopen("input.in", "r", stdin);
	int t;
	cin >> t;
	while(t--)
		solve();
	return 0;
}
