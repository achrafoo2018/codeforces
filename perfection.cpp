void solve(){
	int n;
	while(scanf("%d", &n), n){
		int ans = 1;
		for(int i=2; i < n; i++){
			if(n%i == 0)
				ans += i;
		}
		if(ans == n)
			printf("%5d  PERFECT\n", n); 
		else if(ans > n)
			printf("%5d  ABUNDANT\n", n); 
		else
			printf("%5d  DEFICIENT\n", n); 
	}
}

