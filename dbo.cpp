#define For(i, n) for(int i=0; i < n; i++)

ll mod = 10e9 + 7;

ll power(ll x, ll y){
    ll res = 1;
    x = x % mod;
    if (x == 0) return 0;
    while (y > 0){
        if (y & 1)
            res = (res*x) % mod;
        y = y>>1;
        x = (x % mod) * (x % mod);
    }
    return res % mod;
}

ll xorSum(vll arr, ll n){
    ll bits = 0; 
    for (ll i=1; i <= n; ++i)
        bits |= arr[i];
    ll ans = (bits%mod) * (power(2, n-1) % mod);
    return ans % mod;
}


void solve(){
	ll t;
	cin >> t;
	while(t--){
		ll n, m;
		cin >> n >> m;
		vll arr(n+1);
		bool visited[n+1];
		For(i, m){
			ll l, r, x;
			cin >> l >> r >> x;
			if(l == r){
				arr[l] = x;
				visited[l] = true;
			}
			else{
				for(ll j=l; j < r; j++)
					if(!visited[j]){
						arr[j] = 0;
						visited[j] = true;
					}
				arr[r] = x;
			}
		}
		cout << xorSum(arr, n) % mod << endl;
	}
}

