void solve(){
	int n;
	cin >> n;
	map<pii, int> vp;
	map<int, int> mx, my;
	ll ans = 0;
	For(i, n){
		Point x; x.read();
		ans += mx[x.x] + my[x.y] - vp[mp(x.x, x.y)];
		mx[x.x]++; my[x.y]++; vp[mp(x.x, x.y)]++;
	}
	cout << ans << endl;
}

