#define For(i, n) for(int i=0; i < n; ++i)

struct Point{
	ll x, y;
	void read(){
		cin >> x >> y;
	}
	Point operator -(const Point& b) const{
		return Point{x- b.x, y-b.y};
	}
	void operator -= (const Point& b){
		x -= b.x;
		y -= b.y;
	}
	ll operator *(const Point& b) const{
		return x * b.y - y *b.x;
	}
};

