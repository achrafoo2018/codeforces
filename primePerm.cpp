void solve(){
	string s;
	cin >> s;
	map<char, int> mp;
	for(auto i : s)
		mp[i]++;
	vi primes;
	for(int i=2; i <= s.size(); ++i){
		bool isPrime = true;
		for(int j = 2; j*j <= i; ++j){
			if(i % j == 0){
				isPrime = false;
				break;
			}
		}
		if(isPrime)
			primes.push_back(i);
	}
	vi positions;
	positions.push_back(0);
	for(int i=0; i < primes.size(); ++i){
		if(primes[i] > (s.size()) / 2){
			positions.push_back(primes[i]-1);
		}
	}
	int n = s.size() - positions.size();

	vector<char> ans(s.size());
	char c;
	bool ok = false;
	for(auto j : mp){
		if(j.second >= n){
			mp[j.first] -= n;
			c = j.first;
			ok = true;
			break;
		}
	}
	if(!ok){
		cout << "NO" << endl;
		return;
	}
	for(int i : positions){
		for(auto j : mp){
			if(j.second > 0){
				ans[i] = j.first;
				mp[j.first]--;
				break;
			}
		}
	}
	cout << "YES" << endl;
	for(int i=0; i < ans.size(); ++i){
		if(!ans[i])
			cout << c;
		else
			cout << ans[i];
	}
	cout << endl;
}

